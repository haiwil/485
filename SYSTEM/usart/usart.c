u8 USART1_Send_String(char* string)
{
  u8 len = strlen(string);
  USART1_Send_Data((u8*)string,len);
  return len;
}


void USART1_Send_Data(u8 *buf, u8 len)
{
	u8 t;

	for(t=0; t<len; t++)
	{
		while(!(USART1->SR&USART_FLAG_TXE))	;
		USART_SendData(USART1,buf[t]);
	}
	while(!(USART1->SR&USART_FLAG_TXE))	;
	USART_RX_STA=0;
}





