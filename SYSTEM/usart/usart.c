u8 USART_RX_BUF[USART_REC_LEN];     //接收缓冲,最大USART_REC_LEN个字节.
//接收状态
//bit15，	接收完成标志
//bit14，	接收到0x0d
//bit13~0，	接收到的有效字节数目
u16 USART_RX_STA=0;       //接收状态标记	  
  
void uart_init(u32 bound){
  //GPIO端口设置
  GPIO_InitTypeDef GPIO_InitStructure;
	USART_InitTypeDef USART_InitStructure;
	NVIC_InitTypeDef NVIC_InitStructure;
	 
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_USART1|RCC_APB2Periph_GPIOA, ENABLE);	//使能USART1，GPIOA时钟
  
	//USART1_TX   GPIOA.9
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_9; //PA.9
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;	//复用推挽输出
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA.9
   
  //USART1_RX	  GPIOA.10初始化
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10;//PA10
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;//浮空输入
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA.10  

  //Usart1 NVIC 配置
  NVIC_InitStructure.NVIC_IRQChannel = USART1_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority=3 ;//抢占优先级3
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 3;		//子优先级3
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;			//IRQ通道使能
	NVIC_Init(&NVIC_InitStructure);	//根据指定的参数初始化VIC寄存器
  
  //USART 初始化设置
	USART_InitStructure.USART_BaudRate = bound;//串口波特率
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式
	USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位
	USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;//无硬件数据流控制
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	//收发模式

	USART_ClearFlag(USART1, USART_FLAG_TC);            //
  USART_GetFlagStatus(USART1,USART_FLAG_TC);         //  

  USART_Init(USART1, &USART_InitStructure); //初始化串口1
  USART_ITConfig(USART1, USART_IT_RXNE, ENABLE);//开启串口接受中断
	USART_ITConfig(USART1, USART_IT_IDLE, ENABLE);/* 使能串口1总线空闲中断 */
  USART_Cmd(USART1, ENABLE);                    //使能串口1 
  
	#ifdef Uart_Debug
	USART1_Send_String("usart1 init ok!\n");
	#endif
}



/***************************************************************************************************
 * @fn      USART1_IRQHandler
 *
 * @brief   | SOI |    Data   |  EOI  |
 *          |  1  |   0-Len   |   1   |
 *
 * @return  None
 ***************************************************************************************************/
void USART1_IRQHandler(void)                	//串口1中断服务程序
{
	u8 Res;

	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)  //接收中断(接收到的数据必须是0x0d 0x0a结尾)
	{
		Res =USART_ReceiveData(USART1);//(USART1->DR);	//读取接收到的数据
		USART1_Send_Data(&Res,1);
		if((USART_RX_STA&0x8000)==0)//接收未完成
		{
				if( (USART_RX_STA&0X3FFF) < (USART_REC_LEN) )   
				{
					USART_RX_BUF[USART_RX_STA&0X3FFF]=Res ;
					USART_RX_STA++;	
				}
				else
				{
					USART_RX_STA=0; //接收错误,重新开始	
				}
		 }
	 }
	
	 if ( USART_GetITStatus( USART1, USART_IT_IDLE ) == SET )      //数据帧接收完毕
	 {
        USART_RX_STA|=0x8000;	//接收完成了
				osal_SetInterruptEvent(USART_RX_FINISH_INTERRUPT_EVENT);
		
		    Res = USART_ReceiveData( USART1 );  //由软件序列清除中断标志位(先读USART_SR，然后读USART_DR
    }

} 

/**************************************************************************************************
* @fn		Usart_SendRxMsg
*
* @brief	把串口接收数据打包为消息发送给App_Task
*
* @param	none
*
* @return	NetErrorStatus
**************************************************************************************************/
NetErrorStatus Usart_SendRxMsg( void )
{
  u8 i = 0;
  u8 len = 0;
  OsalUSARTData_t* pkt_msg;

  if( USART_RX_STA&0x8000 )
  {
      len = USART_RX_STA&0X3FFF ;	  
	  if(len > USART_REC_LEN)
	  {
        USART_RX_STA = 0;
  	    return ERROR;
	  }
	  pkt_msg = (OsalUSARTData_t*)osal_msg_allocate( sizeof(OsalUSARTData_t) + len );
	  if( pkt_msg == NULL )
	  {
        USART_RX_STA = 0;
  	    return MEM_FULL;
	  }
	  pkt_msg->length = len;
	  pkt_msg->msg = (u8*)pkt_msg + sizeof(OsalUSARTData_t);
	  
	  for( i=0; i < len; i++ )
	  {
	  	pkt_msg->msg[i] = USART_RX_BUF[i];
	  }
      
	  #ifdef Uart_Debug
        Debug_DealSerialCMD(pkt_msg);
      #endif 
      
	  if( SUCCESS != osal_msg_send( App_TaskID, APP_SERIAL_MSG, (osal_msg_hdr_t *)pkt_msg ,0 ) )
	  {
	  	USART_RX_STA = 0;
	    return INVALID_PARAM;
	  }
	  USART_RX_STA = 0;
	  return SUCCESS;
  }
  else
  {
     return ERROR;
  }
}

